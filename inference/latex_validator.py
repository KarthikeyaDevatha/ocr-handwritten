"""
LaTeX Grammar Validator for OCR Output.
Provides structural and semantic validation of LaTeX expressions
generated by math OCR models, going beyond simple brace balancing.
"""

import re
from typing import List, Dict, Tuple, Optional
from dataclasses import dataclass, field


@dataclass
class ValidationResult:
    """Result of LaTeX validation."""
    is_valid: bool
    corrected_latex: str
    violations: List[Dict] = field(default_factory=list)
    confidence_modifier: float = 1.0  # Multiplier for OCR confidence


